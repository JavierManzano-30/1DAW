public class Numeros {
    public static void main(String[]args) {
        byte byteMax = Byte.MAX_VALUE;
        System.out.println("Byte: " + byteMax);
        short shortMax = Short.MAX_VALUE;
        System.out.println("Short: " + shortMax);
        int integerMax = Integer.MAX_VALUE;
        System.out.println("Integer: " + integerMax);
        long longMax = Long.MAX_VALUE;
        System.out.println("Long: " + longMax);
        float floatMax = Float.MAX_VALUE;
        System.out.println("Float: " + floatMax);
        double doubleMax = Double.MAX_VALUE;
        System.out.println("Double: " + doubleMax);

        byte byteMin = Byte.MIN_VALUE;
        System.out.println("Byte: " + byteMin);
        short shortMin = Short.MIN_VALUE;
        System.out.println("Short: " + shortMin);
        int integerMin = Integer.MIN_VALUE;
        System.out.println("Integer: " + integerMin);
        long longMin = Long.MIN_VALUE;
        System.out.println("Long: " + longMin);
    }
}